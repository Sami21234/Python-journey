
# 3. How do you prevent a python print() function to print a new line at the end. 

print("Mohd Sami")  # By Default new line space due to print
print(20)           # By Default new line space due to print 
print("Developer", end="")  # No line space due to (end="")
print("Compuer Science and Engineering AIML")

# So we will prevent a python print() function to print a new line at the end by using (end="")